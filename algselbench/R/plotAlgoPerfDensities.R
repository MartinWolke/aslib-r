#' @rdname plotAlgoPerfADoc
#' @export
plotAlgoPerfDensities = function(astask, measure, log = FALSE) {
  z = getEDAAlgoPerf(astask, measure, jitter = TRUE, check.log = log, format = "long")
  p = ggplot(z$data, aes_string(x = z$measure, col = "algorithm")) + geom_density()
  if (log)
    p = p + scale_x_log10()
  p
}



